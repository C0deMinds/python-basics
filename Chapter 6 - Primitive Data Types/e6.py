"""
Exercise 6: Write a program that takes two integers as input 
and prints their sum, difference, product, and quotient.
"""

print("Calculator")

n1 = int(input("Give me the first integer: "))  # int() automatically converts it from string to integer
n2 = int(input("Give me the second integer: "))

print(n1 + n2)
print(n1 - n2)
print(n1 * n2)
print(n1 / n2)
